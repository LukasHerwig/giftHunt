CREATE OR REPLACE FUNCTION public.handle_item_taken()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
    -- If item is being marked as taken and wasn't taken before
    IF NEW.is_taken = TRUE AND (OLD.is_taken = FALSE OR OLD.is_taken IS NULL) THEN
        NEW.taken_at = NOW();
    -- If item is being unmarked as taken
    ELSIF NEW.is_taken = FALSE THEN
        NEW.taken_at = NULL;
        NEW.taken_by_name = NULL;
    END IF;
    
    RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
BEGIN
  INSERT INTO public.profiles (id, email, full_name)
  VALUES (NEW.id, NEW.email, NEW.raw_user_meta_data->>'full_name');
  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.update_updated_at_column()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$function$
;



